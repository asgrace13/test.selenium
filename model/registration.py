
class Registration:

    def __init__(self, email=None, surname=None, name=None, patronymic=None,
                 position=None, phone=None, company=None, city=None, site=None,
                 technician=None, manager=None):
        self.email = email
        self.surname = surname
        self.name = name
        self.patronymic = patronymic
        self.position = position
        self.phone = phone
        self.company = company
        self.city = city
        self.site = site
        self.technician = technician
        self.manager = manager
